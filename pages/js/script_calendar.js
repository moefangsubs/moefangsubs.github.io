// Function to create HTML with generation grouping
const createCalendarHTML = (data) => {
  const months = [
    "JANUARI", "FEBRUARI", "MARET", "APRIL", "MEI", "JUNI", 
    "JULI", "AGUSTUS", "SEPTEMBER", "OKTOBER", "NOVEMBER", "DESEMBER"
  ];

  const calendarContainer = document.querySelector('.calender-container');

  // Group members by month
  const groupedMembers = {};

  Object.keys(data).forEach(group => {
    Object.keys(data[group]).forEach(gen => {
		data[group][gen].forEach(member => {
		  const birthDate = parseDate(member[3]);
		  const month = months[birthDate.getMonth()];

		  if (!groupedMembers[month]) {
		    groupedMembers[month] = [];
		  }

		  groupedMembers[month].push({
		    nameJP: member[0],
		    nameRomaji: member[1],
		    img: member[2],
		    birthDate: member[3],
		    groupClass: group,
		    gen: gen,
		    subfolder: member[4] // The subfolder is now part of the member data
		  });
		});
    });
  });

  // Sort months array based on predefined order
  const sortedMonths = months.slice(); // Clone months array
  sortedMonths.sort((a, b) => {
    return months.indexOf(a) - months.indexOf(b);
  });

  // Create HTML for each month
  sortedMonths.forEach(month => {
    if (groupedMembers[month]) {
		const monthDiv = document.createElement('div');
		monthDiv.classList.add('namabulan');
		monthDiv.textContent = month;
		calendarContainer.appendChild(monthDiv);

		const monthListDiv = document.createElement('div');
		monthListDiv.classList.add('calenderlist');

		// Sort members by birth date (day, and if same day, by year)
		groupedMembers[month].sort((a, b) => {
		  const dateA = parseDate(a.birthDate);
		  const dateB = parseDate(b.birthDate);
		  // First by day
		  if (dateA.getDate() !== dateB.getDate()) {
		    return dateA.getDate() - dateB.getDate();
		  }
		  // If day is the same, by year
		  return dateA.getFullYear() - dateB.getFullYear();
		});

		groupedMembers[month].forEach(member => {
		  const memberDiv = document.createElement('div');
		  memberDiv.classList.add(member.groupClass); // Use the specific group class

		  const pictureDiv = document.createElement('div');
		  pictureDiv.classList.add('pictm');

		  // Construct the image URL based on the subfolder stored in the member data
		  const subfolder = member.subfolder || 'default'; // Use member's subfolder, fallback to 'default'
		  const imgURL = `https://ik.imagekit.io/moearchive/calendar/${subfolder}/${member.img}.png`;

		  const img = document.createElement('img');
		  img.src = imgURL;
		  img.alt = member.nameJP; // Set alt text as member's name
		  pictureDiv.appendChild(img);

		  const memberDataDiv = document.createElement('div');
		  memberDataDiv.classList.add('mem-data');

		  const nameJPDiv = document.createElement('div');
		  nameJPDiv.classList.add('jptx', 'jp700');
		  nameJPDiv.textContent = member.nameJP;

		  const nameRomajiDiv = document.createElement('div');
		  nameRomajiDiv.classList.add('romaji');
		  nameRomajiDiv.textContent = member.nameRomaji;

		  const genDiv = document.createElement('div');
		  genDiv.classList.add('gen');
		  genDiv.textContent = member.gen; // Displaying the generation

		  const birthDateDiv = document.createElement('div');
		  birthDateDiv.classList.add('date');
		  birthDateDiv.textContent = formatDate(member.birthDate); // Format the date here

		  const countUpDiv = document.createElement('div');
		  countUpDiv.classList.add('countUp');
		  countUpDiv.setAttribute('data-date', convertDateFormat(member.birthDate));

		  memberDataDiv.appendChild(nameJPDiv);
		  memberDataDiv.appendChild(nameRomajiDiv);
		  memberDataDiv.appendChild(genDiv); // Add the generation to the member data
		  memberDataDiv.appendChild(birthDateDiv);
		  memberDataDiv.appendChild(countUpDiv);

		  memberDiv.appendChild(pictureDiv);
		  memberDiv.appendChild(memberDataDiv);

		  monthListDiv.appendChild(memberDiv);
		});

		calendarContainer.appendChild(monthListDiv);
    }
  });
};

// Function to parse date in "yyyyå¹´mmæœˆddæ—¥" format to Date object
const parseDate = (dateString) => {
  const dateParts = dateString.split('å¹´');
  const monthDayParts = dateParts[1].split('æœˆ');
  const day = monthDayParts[1].split('æ—¥')[0];
  const month = parseInt(dateParts[1].split('æœˆ')[0]) - 1; // JavaScript months are 0-based
  const year = parseInt(dateParts[0]);

  return new Date(year, month, day);
};

// Function to convert date format to "Day Month Year" (e.g., "12 Mei 2002")
const formatDate = (dateString) => {
  const date = parseDate(dateString);
  const monthsInIndonesian = [
    "Januari", "Februari", "Maret", "April", "Mei", "Juni", 
    "Juli", "Agustus", "September", "Oktober", "November", "Desember"
  ];
  const day = date.getDate();
  const month = monthsInIndonesian[date.getMonth()];
  const year = date.getFullYear();
  return `${day} ${month} ${year}`;
};

// Function to convert date format from "yyyyå¹´mmæœˆddæ—¥" to "Month day, year"
const convertDateFormat = (dateString) => {
  const date = parseDate(dateString);
  const options = { year: 'numeric', month: 'long', day: 'numeric' };
  return date.toLocaleDateString('en-US', options);
};

