// Generate HTML
function generateSNSContent() {
    const container = document.querySelector('.sns-container');

    Object.keys(snsData).forEach(key => {
        const data = snsData[key];
        const isMember = imageMembers.hasOwnProperty(key);
        const hasNonMemberImage = imageNonMembers.hasOwnProperty(key);

        const memberDiv = document.createElement('div');
        memberDiv.classList.add('member');

        // Name section
        const nameDiv = document.createElement('div');
        nameDiv.classList.add('name');

        if (isMember) {
            // Logika untuk imageMembers
            const img = document.createElement('img');
            img.src = `${defaultImageBase}${imageMembers[key]}.png`;
            nameDiv.appendChild(img);
        } else if (hasNonMemberImage) {
            // Logika untuk imageNonMembers
            const img = document.createElement('img');
            const imageSrc = imageNonMembers[key];
            img.src = imageSrc.startsWith('http') ? imageSrc : '';
            nameDiv.appendChild(img);
        }

        const roName = document.createElement('p');
        roName.classList.add('ro');
        roName.textContent = data.namero;
        roName.style.fontFamily = "'Funnel Display', serif";

        const jpName = document.createElement('p');
        jpName.classList.add('jp');
        jpName.textContent = data.namejp;
        jpName.style.fontFamily = "'M PLUS Rounded 1c', sans-serif";

        nameDiv.appendChild(roName);
        nameDiv.appendChild(jpName);
        memberDiv.appendChild(nameDiv);

        // Data section
        const dataDiv = document.createElement('div');
        dataDiv.classList.add('data');

        // if (data.web) {
            // const webLink = createLink('web', data.web, 'Official');
            // dataDiv.appendChild(webLink);
        // }

        if (data.web) {
            const webNames = Array.isArray(data.webname) ? data.webname : [data.webname];
            if (Array.isArray(data.web)) {
                data.web.forEach((link, index) => {
                    const ytLink = createLink('web', link, webNames[index] || `Web ${index + 1}`);
                    dataDiv.appendChild(ytLink);
                });
            } else {
                const ytLink = createLink('web', data.web, webNames[0] || 'Website');
                dataDiv.appendChild(ytLink);
            }
        }
		
        if (data.insta) {
            if (Array.isArray(data.insta)) {
                data.insta.forEach(link => {
                    const instaHandle = `@${link.split('/').pop()}`;
                    const instaLink = createLink('insta', link, instaHandle);
                    dataDiv.appendChild(instaLink);
                });
            } else {
                const instaHandle = `@${data.insta.split('/').pop()}`;
                const instaLink = createLink('insta', data.insta, instaHandle);
                dataDiv.appendChild(instaLink);
            }
        }
        if (data.inspb) {
            if (Array.isArray(data.inspb)) {
                data.inspb.forEach(link => {
                    const inspbHandle = `@${link.split('/').pop()}`;
                    const inspbLink = createLink('inspb', link, inspbHandle);
                    dataDiv.appendChild(inspbLink);
                });
            } else {
                const inspbHandle = `@${data.inspb.split('/').pop()}`;
                const inspbLink = createLink('inspb', data.inspb, inspbHandle);
                dataDiv.appendChild(inspbLink);
            }
        }



        if (data.youtube) {
            const ytNames = Array.isArray(data.ytname) ? data.ytname : [data.ytname];
            if (Array.isArray(data.youtube)) {
                data.youtube.forEach((link, index) => {
                    const ytLink = createLink('youtube', link, ytNames[index] || `YouTube Link ${index + 1}`);
                    dataDiv.appendChild(ytLink);
                });
            } else {
                const ytLink = createLink('youtube', data.youtube, ytNames[0] || 'YouTube Link');
                dataDiv.appendChild(ytLink);
            }
        }

        if (data.twitter) {
            if (Array.isArray(data.twitter)) {
                data.twitter.forEach(link => {
                    const twitterHandle = `@${link.split('/').pop()}`;
                    const twitterLink = createLink('twitter', link, twitterHandle);
                    dataDiv.appendChild(twitterLink);
                });
            } else {
                const twitterHandle = `@${data.twitter.split('/').pop()}`;
                const twitterLink = createLink('twitter', data.twitter, twitterHandle);
                dataDiv.appendChild(twitterLink);
            }
        }
		
        if (data.twittpb) {
            if (Array.isArray(data.twittpb)) {
                data.twittpb.forEach(link => {
                    const twittpbHandle = `@${link.split('/').pop()}`;
                    const twittpbLink = createLink('twittpb', link, twittpbHandle);
                    dataDiv.appendChild(twittpbLink);
                });
            } else {
                const twittpbHandle = `@${data.twittpb.split('/').pop()}`;
                const twittpbLink = createLink('twittpb', data.twittpb, twittpbHandle);
                dataDiv.appendChild(twittpbLink);
            }
        }

        if (data.tiktok) {
            const tiktokLink = createLink('tiktok', data.tiktok, `${data.tiktok.split('/').pop()}`);
            dataDiv.appendChild(tiktokLink);
        }

        if (data.weibo) {
            const weiboLink = createLink('weibo', data.weibo, data.weibo.split('/').pop());
            dataDiv.appendChild(weiboLink);
        }

        memberDiv.appendChild(dataDiv);
        container.appendChild(memberDiv);
    });
}

function createLink(className, href, text) {
    const link = document.createElement('a');
    link.classList.add(className);
    link.href = href;
    link.target = '_blank';
    link.textContent = text;
    return link;
}

